	<h2>Gestion des frais des visiteurs</h2>
	<p>Bienvenue dans votre application de gestion des frais de déplacements. </p>
	
		Afin de couvrir les frais qui ont été engager par les visiteurs lors de leurs déplacements professionnels, vous devez vérifier puis valider ou refuser les fiches de frais mensuelles.
		<ul>
			<li>Lorsqu'une fiche est corectement renseignée, vous devrez la "valider" afin qu'elle puisse être traitée pour le rembourssement; </li>
			<li>Vous pouvez modifier certain éléments d'une fiche de frais si ceux-ci sont mal renseignés; </li>
			<li>Si une fiche est mal renseigner, vous devez la "refuser".; </li>
		</ul>
	

	
		Au moyen du bandeau gauche, vous avez accès aux fonctionalités 
		du profil comptable : 
		<ul>
			<li>Gérer les fiches de frais (valider, modifier, refuser)</li>
			<li>Se déconnecter</li>
		</ul>
